const changeLanguage = () => {
  const keyboard = document.querySelector('.keyboard');
  if (curLang === 'ru') {
    curLang = 'en';
    localStorage.setItem('curLang', curLang);
    keyboard.classList.add('lang-en');
    keyboard.classList.remove('lang-ru');
  } else {
    curLang = 'ru';
    localStorage.setItem('curLang', curLang);
    keyboard.classList.add('lang-ru');
    keyboard.classList.remove('lang-en');
  }
};

const createKey = (keyObj) => {
  const textarea = document.querySelector('.textarea');

  const textareaInsert = (insertion) => {
    textarea.focus();
    const selectStart = textarea.selectionStart;
    const selectEnd = textarea.selectionEnd;
    const firstPart = textarea.value.slice(0, selectStart);
    const secondPart = textarea.value.slice(selectEnd, textarea.length);

    textarea.value = `${firstPart}${insertion}${secondPart}`;
    textarea.selectionStart = selectStart + 1;
    textarea.selectionEnd = selectStart + 1;
  };

  const textareaDelete = (isBackspace) => {
    textarea.focus();
    const selectStart = textarea.selectionStart;
    const selectEnd = textarea.selectionEnd;

    if (selectEnd > selectStart) {
      const firstPart = textarea.value.slice(0, selectStart);
      const secondPart = textarea.value.slice(selectEnd, textarea.length);
      textarea.value = `${firstPart}${secondPart}`;
      textarea.selectionStart = selectStart + 1;
      textarea.selectionEnd = selectStart + 1;
    } else if (isBackspace) {
      const firstPart = textarea.value.slice(0, selectStart - 1);
      const secondPart = textarea.value.slice(selectEnd, textarea.length);
      textarea.value = `${firstPart}${secondPart}`;
      textarea.selectionStart = selectStart - 1;
      textarea.selectionEnd = selectStart - 1;
    } else {
      const firstPart = textarea.value.slice(0, selectStart);
      const secondPart = textarea.value.slice(selectEnd + 1, textarea.length);
      textarea.value = `${firstPart}${secondPart}`;
      textarea.selectionStart = selectStart;
      textarea.selectionEnd = selectStart;
    }
  };

  const key = document.createElement('div');
  key.classList.add('key');
  key.innerHTML = `<span class="ru-lower-case">${keyObj.ru.lowerCase}</span>
    <span class="ru-upper-case">${keyObj.ru.upperCase}</span>
    <span class="en-lower-case">${keyObj.en.lowerCase}</span>
    <span class="en-upper-case">${keyObj.en.upperCase}</span>`;

  if (keyObj.keyClass) {
    key.classList.add(keyObj.keyClass);
  }

  key.classList.add(keyObj.eventCode.toLowerCase());

  key.addEventListener('mousedown', (event) => {
    console.log('heymousedown');
    key.dispatchEvent(createCustomEvent('keydown', event, keyObj));
  });

  key.addEventListener('mouseup', (event) => {
    console.log('heymouseup');
    key.dispatchEvent(createCustomEvent('keyup', event, keyObj));
  });

  key.addEventListener('keydown', (event) => {
    console.log('heydown');
    if (keyObj.eventCode === event.code) {
      key.classList.add('active');
      // Change language
      if ((keyObj.eventCode === 'AltLeft' && event.ctrlKey) || (keyObj.eventCode === 'ControlLeft' && event.altKey)) {
        changeLanguage();
        console.log(curLang);
      }

      if (keyObj.eventCode === 'Backspace') {
        textareaDelete(true);
      } else if (keyObj.eventCode === 'Tab') {
        textareaInsert('\t');
      } else if (keyObj.eventCode === 'Delete') {
        textareaDelete(false);
      } else {
        textareaInsert(keyObj[curLang][`${curCase}Case`]);
      }
    }
  });

  key.addEventListener('keyup', (event) => {
    console.log('heyup');
    if (keyObj.eventCode === event.code) {
      key.classList.remove('active');
    }
  });

  return key;
};

const createKeyboard = () => {
  const app = document.createElement('div');
  app.classList.add('app');
  app.innerHTML = `<div class="head">
    <div class="info">
      <p class="info-text">Область ввода:</p>
      <a class="info-link" href="#">Pool Request</a>
    </div>
    <textarea class="textarea"></textarea>
    <div class="info">
      <p class="info-text">OS: Windows.</p>
      <p class="info-text">Переключение языка: левые ctrl + alt.</p>
    </div>
  </div>
  <div class="keyboard lang-${curLang} ${curCase}-case">
    <div class="keys-row"></div>
    <div class="keys-row"></div>
    <div class="keys-row"></div>
    <div class="keys-row"></div>
    <div class="keys-row"></div>
  </div>`;

  body.appendChild(app);
  const keyboard = document.querySelector('.keyboard');

  Object.keys(keys).forEach((key) => {
    if (keys[key].keysRow === 1) {
      keyboard.children[0].appendChild(createKey(keys[key]));
    } else if (keys[key].keysRow === 2) {
      keyboard.children[1].appendChild(createKey(keys[key]));
    } else if (keys[key].keysRow === 3) {
      keyboard.children[2].appendChild(createKey(keys[key]));
    } else if (keys[key].keysRow === 4) {
      keyboard.children[3].appendChild(createKey(keys[key]));
    } else if (keys[key].keysRow === 5) {
      keyboard.children[4].appendChild(createKey(keys[key]));
    }
  });
};

